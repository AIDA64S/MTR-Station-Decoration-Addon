package top.mcmtr.core.data;

public class SolidVectorLocation {
    public final double x1, z1, x2, z2, x3, z3, x4, z4, xs1, zs1, xs2, zs2, xs3, zs3, xs4, zs4, y1, y2;

    public SolidVectorLocation(double x1, double z1, double x2, double z2, double x3, double z3, double x4, double z4, double xs1, double zs1, double xs2, double zs2, double xs3, double zs3, double xs4, double zs4, double y1, double y2) {
        this.x1 = x1;
        this.z1 = z1;
        this.x2 = x2;
        this.z2 = z2;
        this.x3 = x3;
        this.z3 = z3;
        this.x4 = x4;
        this.z4 = z4;
        this.xs1 = xs1;
        this.zs1 = zs1;
        this.xs2 = xs2;
        this.zs2 = zs2;
        this.xs3 = xs3;
        this.zs3 = zs3;
        this.xs4 = xs4;
        this.zs4 = zs4;
        this.y1 = y1;
        this.y2 = y2;
    }
}
